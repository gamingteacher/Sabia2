const FooterNav = () => {
  const { isAuthenticated, isUserAdmin } = useAuthStore()

  const navigationItems = [
    {
      name: 'Ninho',
      path: '/',
      icon: HomeIcon,
      showAlways: true
    },
    {
      name: 'Curadoria',
      path: '/#curadoria',
      icon: CuratoriaIcon,
      showAlways: true
    },
    {
      name: 'Categorias',
      path: '/#categorias',
      icon: CategoriasIcon,
      showAlways: true
    },
    {
      name: 'Ferramentas',
      path: '/#ferramentas',
      icon: FerramentasIcon,
      showAlways: true
    }
  ]

  // Adicionar item admin se o usuário for admin
  if (isAuthenticated() && isUserAdmin()) {
    navigationItems.push({
      name: 'Admin',
      path: '/admin',
      icon: AdminIcon,
      showAlways: false
    })
  }

  return (
    <nav className="fixed bottom-0 left-0 right-0 bg-white border-t border-primary/20 px-4 py-2 z-50">
      <div className="flex justify-around items-center max-w-md mx-auto">
        {navigationItems.map((item) => {
          const IconComponent = item.icon
          
          return (
            <NavLink
              key={item.name}
              to={item.path}
              className={({ isActive }) =>
                `flex flex-col items-center p-2 rounded-lg transition-all duration-200 ${
                  isActive
                    ? 'text-primary bg-primary/10'
                    : 'text-gray-600 hover:text-primary hover:bg-primary/5'
                }`
              }
            >
              <IconComponent />
              <span className="text-xs mt-1 font-medium">{item.name}</span>
            </NavLink>
          )
        })}
      </div>
    </nav>
  )
}

export default FooterNav